/*
while은 자바에서 "반복해서 어떤 일을 시킬 때" 사용하는 명령어
구조 -> while ( 조건 ) {반복할 내용}

ex) 안녕하세요 5번 출력
-----------------------------------------------------------------------------------------------
int count = 0; // 'count'라는 상자를 만들고 0을 넣어둡니다.
while (count < 5) { // count가 5보다 작은 동안 계속 반복해!
System.out.println("안녕하세요"); // "안녕하세요"를 출력하고
count = count + 1; // count에 1을 더해서 다시 count 상자에 넣어줘. (count를 1씩 늘려주는 것)
}
System.out.println("반복 끝!"); // while이 끝나면 이 문장이 실행됩니다.
-----------------------------------------------------------------------------------------------
핵심 정리 : while은 특정 조건이 참(true)인 동안 중괄호 {} 안의 내용을 반복해서 실행하게 해주는 명령어입니다.
조건이 거짓(false)이 되면 반복을 멈춥니다.
주의할 점: 조건을 잘못 설정하면 while이 영원히 끝나지 않고 계속 반복될 수 있어요. 
(이걸 "무한 루프"라고 하는데, 컴퓨터가 멈출 수 있으니 조심해야 해요!)












































*/