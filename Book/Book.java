// ----------------------------------------------------------------------------------------------
// |         비교 항목      |         ArrayList       |               HashMap                   |
// | --------------------- | ----------------------- | ---------------------------------------- |
// |      데이터 접근 방식  |     순서(index) 기반     |        키(key) 기반 (여기선 번호)         |
// | 삭제/삽입 시 성능 문제 |  중간 삽입/삭제 시 느림   |    키로 접근하므로 빠르게 삽입/삭제 가능    |
// |       중복 키 허용    |     인덱스는 중복 가능    |            키는 고유해야 함               |
// |        사용 예시      | 순차적으로 순회할 때 편함 | 고유 키(예: 회원번호)로 빠르게 찾을 때 유리 |
