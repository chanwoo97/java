import java.util.HashMap; // HashMap 클래스를 사용하기 위해 불러오기
import java.util.Map; // Map 인터페이스 사용을 위해 불러오기
import java.util.Scanner; // 사용자 입력을 받기 위해 Scanner 사용

public class Quiz8 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in); // 키보드 입력을 받을 Scanner 객체 생성

        // 음료수 이름과 가격을 저장할 HashMap 생성 (이름:가격)
        Map<String, Integer> drinks = new HashMap<>();
        drinks.put("밀키스", 700); // "밀키스"라는 키에 700원 가격 저장
        drinks.put("코카콜라", 800);
        drinks.put("펩시", 1000);
        drinks.put("칠성사이다", 1200);

        System.out.println("밀키스, 코카콜라, 펩시, 칠성사이다 있습니다."); // 메뉴 안내 출력

        while (true) { // 무한 반복 시작 (사용자가 '그만' 입력 시 종료)
            System.out.print("선택 >> "); // 사용자에게 음료수 이름 입력 요청
            String input = scanner.nextLine().trim(); // 한 줄 입력 받고 앞뒤 공백 제거

            if (input.equals("그만")) { // 만약 입력이 "그만"이면 프로그램 종료
                System.out.println("종료합니다...");
                break; // 반복 종료
            }

            if (drinks.containsKey(input)) { // 입력한 음료수가 HashMap에 있으면
                System.out.println(input + "는 " + drinks.get(input) + "원 입니다."); // 가격 출력
            } else { // 없으면 목록에 없다는 메시지 출력
                System.out.println(input + "은(는) 목록에 없습니다.");
            }
        }

        scanner.close(); // Scanner 자원 해제 (입력 종료)
    }
}

