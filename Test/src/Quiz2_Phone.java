public class Quiz2_Phone {
    // 아래 세 줄은 객체가 가질 '이름', '전화번호', '주소' 정보를 저장하는 공간(필드)입니다.
    private String name;      // 이름을 저장하는 변수 (문자열)
    private String tel;       // 전화번호를 저장하는 변수 (문자열)
    private String address;   // 주소를 저장하는 변수 (문자열)

    // 생성자: 이 클래스의 객체를 만들 때 이름, 전화번호, 주소를 받아서
    // 각 변수에 값을 넣어 초기화해 주는 역할을 합니다.
    public Quiz2_Phone(String name, String tel, String address) {
        this.name = name;         // 입력받은 이름을 객체의 name 변수에 저장
        this.tel = tel;           // 입력받은 전화번호를 객체의 tel 변수에 저장
        this.address = address;   // 입력받은 주소를 객체의 address 변수에 저장
    }

    // 아래 3개의 메서드는 각각 저장된 정보를 꺼내어 돌려주는 역할을 합니다.

    // 저장된 이름을 반환(돌려줌)
    public String getName() {
        return name;
    }

    // 저장된 전화번호를 반환
    public String getTel() {
        return tel;
    }

    // 저장된 주소를 반환
    public String getAddress() {
        return address;
    }
}


